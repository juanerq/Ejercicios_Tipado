//Cree un programa que lea dos números y muestre su producto, su cociente, su suma y su resta.

let numero_1 = parseFloat(prompt("Ingrese un número"));
let numero_2 = parseFloat(prompt("Ingrese otro número"));
let producto = numero_1 * numero_2;
let cociente = numero_1 / numero_2;
let suma = numero_1 + numero_2;
let resta = numero_1 - numero_2;

console.log("El resultado del producto es: ", producto);
console.log("El resultado del cociente es: ", cociente);
console.log("El resultado de la suma es: ", suma);
console.log("El resultado de la resta es: ", resta);

 
console.log("El resultado del producto es ",producto, ",del cociente es ",cociente,
",de la suma es ",suma, "y de la resta es ",resta);